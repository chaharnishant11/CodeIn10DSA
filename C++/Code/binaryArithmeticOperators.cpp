#include <iostream>

using namespace std;

int main(){
    int a = 20;
    int b = 10;

    cout<<"a+b "<<a+b<<endl;  //adding a and b
    cout<<"a-b "<<a-b<<endl;  //subtracting b from a
    cout<<"a*b "<<a*b<<endl;  //multiplying a and b
    cout<<"a/b "<<a/b<<endl;  //dividing b by a
    cout<<"a%b "<<a%b<<endl;  //remainder on dividing b by a
}

/*
OUTPUT
a+b 30
a-b 10
a*b 200
a/b 2
a%b 0
*/